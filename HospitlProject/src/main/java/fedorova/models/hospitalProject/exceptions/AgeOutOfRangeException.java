package fedorova.models.hospitalProject.exceptions;

public class AgeOutOfRangeException extends Exception {

	public AgeOutOfRangeException() {
		super();
	}

	public AgeOutOfRangeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AgeOutOfRangeException(String message, Throwable cause) {
		super(message, cause);
	}

	public AgeOutOfRangeException(String message) {
		super(message);
	}

	public AgeOutOfRangeException(Throwable cause) {
		super(cause);
	}
		
	


}
