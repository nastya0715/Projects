package fedorova.models.hospitalProject.exceptions;

public class GenderOutOfNameException extends Exception {

	public GenderOutOfNameException() {
		super();
	}

	public GenderOutOfNameException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GenderOutOfNameException(String message, Throwable cause) {
		super(message, cause);
	}

	public GenderOutOfNameException(String message) {
		super(message);
	}

	public GenderOutOfNameException(Throwable cause) {
		super(cause);
	}

	
}
